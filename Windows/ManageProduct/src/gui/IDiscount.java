    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int index = this.tblDiscount.getSelectedRow();
        if(this.discounts.get(index).getId() == 1){
            JOptionPane.showMessageDialog(this, "Không được chỉnh sử discount này!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        Discount discount = this.discounts.get(index);
        this.pnlDetail.setVisible(true);
        this.txtPercent.setValue(discount.getPercentDiscount());
        Date dateB = discount.getTimeBegin();
        Date dateE = discount.getTimeEnd();
        this.txtDate.setValue(dateB.getDate());
        this.txtMonth.setValue(dateB.getMonth() + 1);
        this.txtYear.setValue(dateB.getYear() + 1900);
        this.txtDate1.setValue(dateE.getDate());
        this.txtMonth1.setValue(dateE.getMonth() + 1);
        this.txtYear1.setValue(dateE.getYear() + 1900);
        this.create = false;
    }//GEN-LAST:event_btnEditActionPerformed


