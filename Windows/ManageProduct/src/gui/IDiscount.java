    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        // TODO add your handling code here:
        try {
            int percentDiscount = (int) this.txtPercent.getValue();
            String dateBeginS = this.txtDate.getValue().toString() + "-" + this.txtMonth.getValue().toString() + "-" + this.txtYear.getValue().toString();
            String dateEndS = this.txtDate1.getValue().toString() + "-" + this.txtMonth1.getValue().toString() + "-" + this.txtYear1.getValue().toString();
            Date dateBegin = CommonFunction.parseDate(dateBeginS, "dd-MM-yyyy");
            Date dateEnd = CommonFunction.parseDate(dateEndS, "dd-MM-yyyy");
            boolean ok = false;
            if (create) {
                Discount discount = new Discount(percentDiscount, dateBegin, dateEnd, null);
                ok = DatabaseAPI.saveOrUpdateDiscount(discount);
            } else {
                Discount discount = new Discount(percentDiscount, dateBegin, dateEnd, null);
                discount.setId(this.discounts.get(this.tblDiscount.getSelectedRow()).getId());
                ok = DatabaseAPI.updateDiscount(discount);
            }
            if (ok) {
                JOptionPane.showMessageDialog(this, "Xử lý ưu đãi thành công!", "Information", JOptionPane.INFORMATION_MESSAGE);
                loadData(page);
                this.btnEdit.setEnabled(false);
            } else {
                JOptionPane.showMessageDialog(this, "Xử ưu đãi thất bại!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Kiểm tra lại thông tin nhập!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnNewActionPerformed

